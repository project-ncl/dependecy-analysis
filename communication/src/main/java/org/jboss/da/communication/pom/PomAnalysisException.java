package org.jboss.da.communication.pom;

public class PomAnalysisException extends Exception {

    public PomAnalysisException(String message) {
        super(message);
    }

    public PomAnalysisException(String message, Throwable cause) {
        super(message + ": " + cause.getMessage(), cause);
    }

    public PomAnalysisException(Throwable cause) {
        super(cause);
    }
}
